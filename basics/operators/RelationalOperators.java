
public class RelationalOperators {
    public static void main(String[] args) {
        int a = 5, b = 10; 

        // Equals to 
        System.out.println(a == b);

        // Not Equals to 
        System.out.println(a != b);

        // Greater than
        System.out.println(a > b); 

        // Less than
        System.out.println(a < b); 

        // Greater than Equal to
        System.out.println(a >= b);

        // Less than than Equal to
        System.out.println(a <= b);
    }
}
